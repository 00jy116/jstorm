package com.alipay.dw.jstorm.stats;

public class Pair {
    private Long first;
    private Long second;
    
    public Pair() {
        this.first = 0l;
        this.second = 0l;
    }
    
    public Long getFirst() {
        return first;
    }
    
    public void setFirst(Long first) {
        this.first = first;
    }
    
    public Long getSecond() {
        return second;
    }
    
    public void setSecond(Long second) {
        this.second = second;
    }
    
}
